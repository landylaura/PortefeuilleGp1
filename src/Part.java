/**
 * Classe qui permet d'associer  une action son pourcentage de detention dans une action compose
 * @author 21400895
 *
 */
public class Part {
	private float pourcentage ;
	
    /**
     *Constructeur d'une part
     * @param p
     */
    public Part(float p){
		this.pourcentage = p;
	}
	
    /**
     *
     * @return
     */
    public float getPourcentage() {
		return pourcentage;
	}
	
	

}
