/**
 * Classe qui permet d'associer à une action son pourcentage de détention dans une action composée
 * @author 21400895
 *
 */
public class Part {
	private float pourcentage ;
	
	public Part(float p){
		this.pourcentage = p;
	}
	
	public float getPourcentage() {
		return pourcentage;
	}
	
	

}
